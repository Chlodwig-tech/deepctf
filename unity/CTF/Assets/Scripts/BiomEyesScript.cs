    void Awake()
    {
        int halfLength = eyes.GetLength(0) / 2;

        for (int i = 0; i < eyes.GetLength(0); i++)
        {
            for (int j = 0; j < eyes.GetLength(1); j++)
            {
                if (eyes[i, j] == 1)
                {
                    SpawnBiomEyeAt(i - halfLength, j);
                }
            }
        }
    }


